// ── Computed helper (mirrors PayslipGenerator._buildData) ─────────────────
export function computePayslip(emp) {
  const ratio = emp.monthDays > 0 ? emp.pDays / emp.monthDays : 1;
  const grossSalary = emp.basic + emp.hra + emp.organisationAllowance + emp.medicalAllowance;
  const grossSalaryD = grossSalary * ratio;
  const totalDeduction = emp.pfDeduction + emp.pt + emp.otherDeduction + emp.tds + emp.advance;
  const netSalary = grossSalaryD - totalDeduction;
  const perfD = emp.performancePay * ratio;
  const totalWithPerf = netSalary + perfD;
  return { grossSalary, grossSalaryD, totalDeduction, netSalary, perfD, totalWithPerf };
}

